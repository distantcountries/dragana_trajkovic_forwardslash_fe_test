export default class WatchesService {
    getAll() {
        return watches;
    }

    addStar(watchId) {
        let watch = watches.find(watch => watch.id == watchId)
        if(watch.star === false) {
            return watch.star = true
        } else {
            return watch.star = false
        }
    }    

    increment(watch) {
        if( watch.orderQuantity === 0) {
            watch.addToCart = false
        }
        if(watch.orderQuantity < watch.quantity) {
            watch.orderQuantity++
            if(watch.orderQuantity > 1) {
                watch.totalAmountOrdered += watch.salePrice
            }
        }
    }

    decrement(watch) {
        if(watch.orderQuantity < 1) {
            watch.orderQuantity = 0
            watch.addToCart = false
        } else {
            watch.orderQuantity--
            watch.totalAmountOrdered -= watch.salePrice
        } 
    }

    showMoreDetails(watchId) {
        let watch = watches.find(watch => watch.id == watchId)
        return watch.moreDetails = true
    }

    hideMoreDetails(watchId) {
        let watch = watches.find(watch => watch.id == watchId)
        return watch.moreDetails = false
    }

    addWatch(watch) {
        if(watch.orderQuantity===0) {
            watch.orderQuantity=1
            watch.addToCart = true
        }
        if(watch.orderQuantity>0) {
            watch.addToCart = true
            watch.totalAmountOrdered = watch.orderQuantity * watch.salePrice
        } else {
            alert('Please select item first')
        }
    }

    showImage(watchImg, watchId) {
        let watch = watches.find(watch => watch.id == watchId)
        return watch.selectedImg = watchImg.url
    }
}

export const watchesService = new WatchesService()